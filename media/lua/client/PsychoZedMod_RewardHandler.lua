--[[ 

PsychoZedMod = PsychoZedMod or {}

function PsychoZedMod.SpawnRewards(zed)
	local itemList = PsychoZedMod.parseItems()
	local sq = zed:getSquare()
	if sq then
		for _, item in ipairs(itemList) do
			local roll = PsychoZedMod.getRewardRoll()
			if roll then
				sq:AddWorldInventoryItem(item, ZombRand(0, 3), ZombRand(0, 3), 0)
			end
		end
	end
end

function PsychoZedMod.getRewardRoll()
	local chance = SandboxVars.PsychoZed.DropRate or 20
	if chance == 0 then return false end
	if chance == 100 then return true end
	return PsychoZedMod.doRoll(chance)
end

function PsychoZedMod.getDeathRewards()
	return SandboxVars.PsychoZed.DeathRewards or 'Base.HuntingKnife;Base.RippedSheetsDirty'
end

function PsychoZedMod.parseItems()
    local tab = {}
	local lootStr = PsychoZedMod.getDeathRewards()
    for item in string.gmatch(lootStr, "([^;]+)") do
        table.insert(tab, item)
    end
    return tab
end


function PsychoZedMod.RewardHandler(zed)
	if not PsychoZedMod.isPsychoZed(zed) then return end
	PsychoZedMod.SpawnRewards(zed)
end
Events.OnZombieDead.Remove(PsychoZedMod.RewardHandler)
Events.OnZombieDead.Add(PsychoZedMod.RewardHandler)

