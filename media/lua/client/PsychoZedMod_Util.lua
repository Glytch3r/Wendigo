function PsychoZedMod.checkDist(pl, zed)
	local dist = pl:DistTo(zed:getX(), zed:getY())
    return math.floor(dist)
end

function PsychoZedMod.isWithinRange(pl, zed, range)
	local dist = pl:DistTo(zed:getX(), zed:getY())
    return dist <= range
end

function PsychoZedMod.isClosestPl(pl, zed)
	if not PsychoZedMod.isPsychoZed(zed) then return end
	local plDist = PsychoZedMod.checkDist(pl, zed)
	local compare = round(zed:distToNearestCamCharacter())
	if plDist == compare then
		return true
	end
	return false
end

