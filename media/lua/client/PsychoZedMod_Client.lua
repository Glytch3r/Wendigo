PsychoZedMod = PsychoZedMod or {}

local Commands = {};
Commands.PsychoZed = {};


-----------------------            ---------------------------
function PsychoZedMod.coreFunc(zed)
    if not zed then return end
    if PsychoZedMod.isPsychoZed(zed) and not zed:getVariableBoolean('isPsychoZed') then
        --PsychoZedMod.setStats(zed)
        zed:setVariable('isPsychoZed', 'true')
        if isClient() then
            sendClientCommand('PsychoZed', 'isPsychoZed', {isPsychoZed = true, zedID = zed:getOnlineID()})
        end
    elseif not PsychoZedMod.isPsychoZed(zed) and zed:getVariableBoolean('isPsychoZed') then
        zed:setVariable('isPsychoZed', 'false')
        if isClient() then
            sendClientCommand('PsychoZed', 'isPsychoZed', {isPsychoZed = false, zedID = zed:getOnlineID()})
        end
    end
end

Events.OnZombieUpdate.Remove(PsychoZedMod.coreFunc)
Events.OnZombieUpdate.Add(PsychoZedMod.coreFunc)


-----------------------            ---------------------------


function PsychoZedMod.findzedID(int)
	local zombies = getCell():getObjectList()
	for i=zombies:size(),1,-1 do
		local zed = zombies:get(i-1)
		if instanceof(zed, "IsoZombie") then
			local zedID=zed:getOnlineID()
			if zedID and zedID == int then return zed end
		end
	end
	return nil
end

-----------------------            ---------------------------

Commands.PsychoZed.isPsychoZed = function(args)
    local source = getPlayer();
    local player = getPlayerByOnlineID(args.id)
    local zedID = args.zedID
    if type(zedID) == 'string' then zedID = tonumber(zedID) end
    local zed = PsychoZedMod.findzedID(zedID)
    if zed ~= nil then
		if source ~= player then
			if args.isPsychoZed then
				zed:setVariable('isPsychoZed', 'true');
			else
				zed:setVariable('isPsychoZed', 'false');
			end
		end
    end
end
Commands.PsychoZed.knockDownZed = function(args)
    local source = getPlayer();
    local player = getPlayerByOnlineID(args.id)
    local zedID = args.zedID
    if type(zedID) == 'string' then zedID = tonumber(zedID) end
    local zed = PsychoZedMod.findzedID(zedID)
	if source ~= player then
		if zed ~= nil then
			zed:setKnockedDown(true)
		end
	end
end

-----------------------            ---------------------------

Commands.PsychoZed.isWearingPsychoZed = function(args)
    local source = getPlayer();
    local player = getPlayerByOnlineID(args.id)
    if source ~= player then
        if args.isWearingPsychoZed then

            PsychoZedMod.wearPsychoZed(player, int)
			--if not player:isHideWeaponModel() then player:setHideWeaponModel(true) end
            if player:getVariableBoolean('isWearingPsychoZed') == false then
                player:setVariable('isWearingPsychoZed', true);
            end
        else

            PsychoZedMod.clearPsychoZedSkin(player)
			--if player:isHideWeaponModel() then player:setHideWeaponModel(false) end
            if player:getVariableBoolean('isWearingPsychoZed')  then
                player:setVariable('isWearingPsychoZed', false);
            end
        end
    end
end

Commands.PsychoZed.Lacerate = function(args)
    local source = getPlayer();
   --local player = getPlayerByOnlineID(args.id)
	local targ = getPlayerByOnlineID(args.targID)
    if source ~= targ then
		PsychoZedMod.doLacerate(targ)
    end
end




Events.OnServerCommand.Add(function(module, command, args)
	if Commands[module] and Commands[module][command] then
		Commands[module][command](args)
	end
end)
