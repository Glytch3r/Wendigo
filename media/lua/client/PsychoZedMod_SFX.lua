function PsychoZedMod.preSFX(zed)

	if not PsychoZedMod.isPsychoZed(zed) then return end
	local pl = getPlayer(); if not pl then return end
	if zed:getEmitter() then
		local audioName = nil
		local list = PsychoZedMod.sfxBanList
		for _, audioName in ipairs(list) do
			if zed:getEmitter():isPlaying(audioName) then
				zed:getEmitter():stopSoundByName(tostring(audioName))
				getWorld():getFreeEmitter():stopSoundByName(tostring(audioName))

				if PsychoZedMod.doRoll(SandboxVars.PsychoZed.SFXRate) then
					if PsychoZedMod.isDay() then
						PsychoZedMod.playSFX(zed, PsychoZedMod.getCrySfx())

					end

					if PsychoZedMod.isNight() and PsychoZedMod.isLaughing(zed) then
						PsychoZedMod.playSFX(zed, PsychoZedMod.getLaughSfx())
					end
				end

			end
		end
	end
end
Events.OnZombieUpdate.Remove(PsychoZedMod.preSFX)
--Events.OnZombieUpdate.Add(PsychoZedMod.preSFX)

function PsychoZedMod.playPlSfx(pl, sfx, attract)
	if not PsychoZedMod.isWearingPsychoZed(pl) then return end
	local sq = pl:getSquare()
	if sq then
		getSoundManager():PlayWorldSound(tostring(sfx), pl:getSquare(), 0, 5, 5, false);
		if attract then
			addSound(pl, pl:getX(), pl:getY(), pl:getZ(), 5, 1);
		end
	end
end


function PsychoZedMod.keypress(key)
	local pl = getPlayer()

    if key == getCore():getKey("Shout") then
		if PsychoZedMod.isWearingPsychoZed(pl)   then

			local sfx = nil
			if pl:isSneaking() then
				sfx = PsychoZedMod.getCrySfx()
				PsychoZedMod.playPlSfx(pl, sfx, false)
			elseif  pl:IsAiming()  then
				sfx = PsychoZedMod.getScreamSfx()
				PsychoZedMod.playPlSfx(pl, sfx, true)
			else
				sfx = PsychoZedMod.getLaughSfx()
				PsychoZedMod.playPlSfx(pl, sfx, false)
			end

		end

    end
	return key
end
Events.OnKeyPressed.Remove(PsychoZedMod.keypress)
Events.OnKeyPressed.Add(PsychoZedMod.keypress)

