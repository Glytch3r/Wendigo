PsychoZedMod = PsychoZedMod or {}


function PsychoZedMod.isDisableTrip()
	return SandboxVars.PsychoZed.DisableTrip
end

function PsychoZedMod.tripHandler(zed)
	if not PsychoZedMod.isPsychoZed(zed)  then return end
	if not PsychoZedMod.isDisableTrip() then return end
	if zed:getVariableString('BumpType') == 'trippingFromSprint' then
		zed:SayDebug('Trip Prevented')
		zed:ClearVariable('BumpType')
		zed:setVariable('FallOnFront', true)
		zed:setVariable('SprinterTripped', false)
		zed:setVariable('Zombie_Sprint', true)
		zed:setVariable('BumpAnimFinished', true)
		--zed:ClearVariable('trippingFromSprint')
		PsychoZedMod.setSprinter(zed)
	end
end
Events.OnZombieUpdate.Remove(PsychoZedMod.tripHandler)
